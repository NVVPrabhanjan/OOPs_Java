package Part5_Methods;

public class PredefinedMethods {
    public static void main(String[] args) {
        int maxVal = Math.max(10, 20);
        System.out.println("Predefined Method (Math.max): Max of 10 and 20 is " + maxVal);

        String upper = "hello".toUpperCase();
        System.out.println("Predefined Method (String.toUpperCase): " + upper);
    }
}
